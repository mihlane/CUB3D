#include "Parsing_Cub3d.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = ft_strlen(s1) - 1;
	j = 3;
	while (j >= 0 && s1[i] == s2[j])
	{
		i--;
		j--;
	}
	if (j == -1)
		return (0);
	return (-1);
}
